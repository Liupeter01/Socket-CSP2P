          int m_socketSizeInfo = sizeof(SOCKADDR_IN);
#else
          socklen_t m_socketSizeInfo = sizeof(SOCKADDR_IN);
#endif // _WIN3264
          bool m_socketStatus = false;         //SOCKET连接
